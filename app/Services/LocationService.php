    public function isPointInPolygon($point, $polygon)
    {
        $x = $point[0];
        $y = $point[1];
        $inside = false;

        $count = count($polygon);

        for ($i = 0, $j = $count - 1; $i < $count; $j = $i++) {
            $xi = $polygon[$i][0];
            $yi = $polygon[$i][1];
            $xj = $polygon[$j][0];
            $yj = $polygon[$j][1];

            $intersect = (($yi > $y) != ($yj > $y)) &&
                ($x < ($xj - $xi) * ($y - $yi) / ($yj - $yi) + $xi);

            if ($intersect) {
                $inside = !$inside;
            }
        }

        return $inside;
    }

    public function isInBekasi($lat, $lng)
    {
        return $this->isPointInPolygon([$lng, $lat], $this->bekasiPolygon);
    }

    public function isInBogor($lat, $lng)
    {
        return $this->isPointInPolygon([$lng, $lat], $this->bogorPolygon);
    }

    public function getPolygon()
    {
        return $this->bekasiPolygon;
    }
    
